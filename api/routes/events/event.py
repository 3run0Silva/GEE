class Event:
    def __init__(self, id="", img="", title="", date="", day=None, month=None, year=None, description="", tag=""):
        self.id = id
        self.img = img
        self.title = title
        self.date = date
        self.day = day
        self.month = month
        self.year = year
        self.description = description
        self.tag = tag
